package com.glenware.spring4;

public class Cyclist {

	private Bicycle bicycle;
	private String cyclingSong;

	public Bicycle getBicycle() {
		return bicycle;
	}

	public void setBicycle(Bicycle bicycle) {
		this.bicycle = bicycle;
	}

	public String getCyclingSong() {
		return cyclingSong;
	}

	public void setCyclingSong(String cyclingSong) {
		this.cyclingSong = cyclingSong;
	}

}